var a = 2, b = 345;

function addition(x, y) {
    return x + y;
}

function subtraction(x, y) {
    return x - y;
}

function multiplication(x, y) {
    return x * y;
}

function division(x, y) {
    return x / y
}

alert(`Задание №5
Для чисел ${a} и ${b} верны следующие равенсва: 
Сложение = ${addition(a, b)}
Вычитание = ${subtraction(a, b)}
Умножение = ${multiplication(a, b)}
Деление = ${division(a, b)}`)
